# Diccionario original
original = {1: "Primero", 2: "Segundo", 3: "Tercero", 4: "Cuarto"}
# Copia del diccionario original
copia = original.copy()
# Agregar una nueva clave y su valor al diccionario copiado
copia[5] = "Quinto"
# Mostrar ambos diccionarios
print("Diccionario original:", original)
print("Diccionario copiado:", copia)